/* 3b) Write a Java program that takes a number and prints its square root, power, and absolute 
value using java.lang.Math 
Code: */
class MathExample { 
public static void main(String[] args) { 
double number = 16.0; 
System.out.println("Square root: " + Math.sqrt(Math.abs(number))); 
System.out.println("Power (2^3): " + Math.pow(2, 3)); 
System.out.println("Absolute value: " + Math.abs(number)); 
} 
} 